export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug)
}

export function getProductsByCategory(category: string): Product[] {
  return products.filter(p => p.category === category)
}

export function getProductsBySubcategory(subcategory: string): Product[] {
  return products.filter(p => p.subcategory === subcategory)
}

export function getProductsByAgeGroup(ageGroup: string): Product[] {
  return products.filter(p => p.ageGroup === ageGroup)
}

export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return products
    .filter(p => p.slug !== product.slug && (p.category === product.category || p.ageGroup === product.ageGroup))
    .slice(0, limit)
}

export function formatPrice(price: number): string {
  return `kr ${price.toLocaleString('nb-NO')}`
}
